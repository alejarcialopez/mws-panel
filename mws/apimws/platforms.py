class PlatformsAPINotWorkingException(Exception):
    pass


def get_api_secret():
    if platform.system() == 'Darwin':
        from passlib.hash import sha512_crypt
        return sha512_crypt.encrypt("7d503557b69ecd94c921784541f18bc2f9ecdd62", rounds=5000,
                                    implicit_rounds=True)  # Salt autogenerated
    return crypt.crypt("7d503557b69ecd94c921784541f18bc2f9ecdd62", "$6$"+''.join(random.sample(string.hexdigits, 16)))


